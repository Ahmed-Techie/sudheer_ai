public class Calculation {
    public static void main(String[] args) {
        int num1 = 50;
        int num2 = 10;
        int cal = num1 * num2;
        System.out.println("Value :" + cal);
        System.out.println(++num1);
        System.out.println(num2++);
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(++cal);
        System.out.println(cal++);
        System.out.println(cal);
        
    }
}